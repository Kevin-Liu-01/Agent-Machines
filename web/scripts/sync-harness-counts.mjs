#!/usr/bin/env node
/**
 * Write edge-safe harness counts for opengraph-image (no JSON/loadout imports).
 * Run after sync-skills / sync-mcp-catalog so OG pills stay registry-accurate.
 */

import { writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { spawnSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const WEB_ROOT = resolve(HERE, "..");
const OUT_FILE = join(WEB_ROOT, "lib/platform/harness-counts.ts");

const evalScript = `
import { HARNESS } from "./lib/platform/harness.ts";
console.log(JSON.stringify({
  skillCount: HARNESS.skillCount,
  serviceRouteCount: HARNESS.serviceRouteCount,
  cliCount: HARNESS.cliCount,
  mcpServerCount: HARNESS.mcpServerCount,
}));
`;

const result = spawnSync("npx", ["tsx", "--eval", evalScript], {
	cwd: WEB_ROOT,
	encoding: "utf8",
});

if (result.status !== 0) {
	console.error(result.stderr || result.stdout);
	process.exit(result.status ?? 1);
}

const counts = JSON.parse(result.stdout.trim());

const source = `/**
 * Edge-safe harness stat slice for \`app/opengraph-image.tsx\`.
 *
 * Generated by \`npm run sync-harness-counts\` — do not edit by hand.
 * Full registry-derived stats live in \`harness.ts\`.
 */
export const HARNESS_COUNTS = {
\tskillCount: ${counts.skillCount},
\tserviceRouteCount: ${counts.serviceRouteCount},
\tcliCount: ${counts.cliCount},
\tmcpServerCount: ${counts.mcpServerCount},
} as const;
`;

writeFileSync(OUT_FILE, source);
console.log(`sync-harness-counts: wrote ${OUT_FILE}`);
